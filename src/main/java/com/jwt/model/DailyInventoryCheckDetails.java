package com.jwt.model;

import java.math.BigDecimal;

public class DailyInventoryCheckDetails {
private String product_name ;
private String type;
private String product_sku;
private String dms_product_id;
private String treatment_name;
private BigDecimal total_quantity;
public String getProduct_name() {
	return product_name;
}
public void setProduct_name(String product_name) {
	this.product_name = product_name;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
public String getProduct_sku() {
	return product_sku;
}
public void setProduct_sku(String product_sku) {
	this.product_sku = product_sku;
}
public String getDms_product_id() {
	return dms_product_id;
}
public void setDms_product_id(String dms_product_id) {
	this.dms_product_id = dms_product_id;
}
public String getTreatment_name() {
	return treatment_name;
}
public void setTreatment_name(String treatment_name) {
	this.treatment_name = treatment_name;
}
public BigDecimal getTotal_quantity() {
	return total_quantity;
}
public void setTotal_quantity(BigDecimal total_quantity) {
	this.total_quantity = total_quantity;
}
}
